CREATE TABLE PULL_RQ (
    PR_ID INT PRIMARY KEY,
    ISSUE_ID INT,
    REPO_ID INT,
    USER_ID INT,
    TITLE VARCHAR(255),
    BODY TEXT,
    STATE VARCHAR(50),
    CREATED_AT DATETIME,
    UPDATED_AT DATETIME,
    CLOSED_AT DATETIME,
    MERGED_AT DATETIME,
    MERGE_COMMIT_SHA VARCHAR(40),
    HEAD_REF VARCHAR(255),
    BASE_REF VARCHAR(255),
    MERGEABLE BOOLEAN,
    MERGED BOOLEAN,
    COMMENTS_COUNT INT,
    REVIEW_COMMENTS_COUNT INT,
    COMMITS_COUNT INT,
    ADDITIONS INT,
    DELETIONS INT,
    CHANGED_FILES_COUNT INT
);